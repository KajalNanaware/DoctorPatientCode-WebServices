package drPatients;

public class Patient {

	private String patientName;
	private String patientInsuranceNum;	
	
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	
	public String getPatientInsuranceNum() {
		return patientInsuranceNum;
	}
	public void setPatientInsuranceNum(String patientInsuranceNum) {
		this.patientInsuranceNum = patientInsuranceNum;
	}
}
